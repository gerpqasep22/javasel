package sep22.day2.operatorsandcontrolstatements;

public class LearnBreakJumpStatement {

	public static void main(String[] args) {
		
		for (int i = 1; i<5; i++)
		{
			if (i==3)
			{
				System.out.println("Three");
				break;
			}
			
			System.out.println(i);
			
		}
		

	}

}
